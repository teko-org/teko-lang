// Validates whether the emitter generates the correct opcodes and manages the String Pool without duplicating items
void test_bytecode_emission_and_constant_pooling(void) {
    BytecodeBuffer* buffer = codegen_li_create_context();
    TEST_ASSERT_NOT_NULL(buffer);

    // Adds repeated string constants in an induced manner
    int idx1 = codegen_li_add_string_constant(buffer, "abc");
    int idx2 = codegen_li_add_string_constant(buffer, "abc");
    int idx3 = codegen_li_add_string_constant(buffer, "xyz");

    // Validates that the constant pool rejected the duplicate and optimized the indices
    TEST_ASSERT_EQUAL_INT(idx1, idx2);
    TEST_ASSERT_EQUAL_INT(0, idx1);
    TEST_ASSERT_EQUAL_INT(1, idx3);

    // Instantiates a dummy constant variable declaration statement (const constant = "abc";)
    StatementASTNode stmt;
    stmt.type = NODE_VAR_DECL;
    stmt.data.var_decl.is_mutable = false;
    stmt.data.var_decl.var_name = strdup("constant");
    stmt.data.var_decl.initializer_raw = strdup("\"abc\"");
    stmt.data.var_decl.var_type = NULL;

    // Emits the bytecode for the statement
    codegen_li_emit_statement(buffer, &stmt);

    // The generated byte stream should contain: OP_SCONST (0x02) + pool index (0) + OP_STORE (0x04)
    TEST_ASSERT_TRUE(buffer->size > 0);
    TEST_ASSERT_EQUAL_UINT8(OP_SCONST, buffer->code[0]);

    // Executes a simulated write to disk to verify the magic header
    codegen_li_write_to_file(buffer, "test_output.tkb");

    // Cleanup
    free(stmt.data.var_decl.var_name);
    free(stmt.data.var_decl.initializer_raw);
    codegen_li_free_context(buffer);
}

// Phase 11 (Browser FFI frontend FE-B): bridge a frontend IL BytecodeBuffer to the
// WASM backend (codegen_li_emit_wasm) and assert the emitted .wat carries the import,
// the call site, and the string pool data segment — IL -> WASM without a parser.
void test_codegen_li_to_wasm_bridge(void) {
    BytecodeBuffer* buffer = codegen_li_create_context();
    TEST_ASSERT_NOT_NULL(buffer);

    int imp = codegen_li_add_import(buffer, "env", "log", 1, 0);
    int s = codegen_li_add_string_constant(buffer, "hi");
    codegen_li_emit_sconst(buffer, s);
    codegen_li_emit_call_import(buffer, imp);
    codegen_li_emit_halt(buffer);

    TekoTarget target;
    target.arch = ARCH_WASM32;
    target.os = OS_WASI;
    strncpy(target.target_string, "wasm32-wasi", sizeof(target.target_string) - 1);

    const char* wat = "output_li_wasm_bridge.wat";
    int rc = codegen_li_emit_wasm(buffer, wat, target, NULL, NULL, NULL, NULL, 0);
    TEST_ASSERT_EQUAL_INT(0, rc);

    FILE* f = fopen(wat, "r");
    TEST_ASSERT_NOT_NULL(f);
    char* out = (char*)malloc(16384);
    TEST_ASSERT_NOT_NULL(out);
    memset(out, 0, 16384);
    size_t n = fread(out, 1, 16383, f);
    out[n] = '\0';
    fclose(f);

    TEST_ASSERT_NOT_NULL(strstr(out, "(import \"env\" \"log\" (func $import_0 (param i32)))"));
    TEST_ASSERT_NOT_NULL(strstr(out, "call $import_0"));
    TEST_ASSERT_NOT_NULL(strstr(out, "(data (i32.const 1024) \"hi\\00\")"));

    free(out);
    remove(wat);
    codegen_li_free_context(buffer);
}

